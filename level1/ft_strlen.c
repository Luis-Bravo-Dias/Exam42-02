#include <stdio.h>

int	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count])
		count++;
	return (count);
}

int main()
{
	char	*str;

	str = "Hello world!";
	printf("%d\n", ft_strlen(str));
}
