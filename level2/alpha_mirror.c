#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	c;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			c = av[1][i];
			if ('A' <= av [1][i] && 'Z' >= av[1][i])
				c = 'Z' - av[1][i] + 'A';
			if ('a' <= av[1][i] && 'z' >= av[1][i])
				c = 'z' - av[1][i] + 'a';
			write(1, &c, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}